package com.example.lijo.cloverboard;


class ThemeItems {
    int image;
    String title;
    public ThemeItems(int image, String title) {
        super();
        this.image = image;
        this.title = title;
    }
    public int getImage() {
        return image;
    }
    public String getTitle() {
        return title;
    }
}